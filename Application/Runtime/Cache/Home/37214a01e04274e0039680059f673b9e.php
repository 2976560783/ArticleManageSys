              <b>2. 汇总
笔记的汇总，也需要分纸质书和电子书两个渠道。</b>
纸书笔记汇总

这个比较麻烦，没有完美的解决方案，而我是这样做的：

一般情况下，将书中所勾画内容的重要部分进行拍照；
十分重要的书，会将勾画内容手打出来。
电子书笔记汇总

电子书就简单多了，就我阅读的主力工具Kindle而言，每一次在书中进行勾画的内容，都会记录在Kindle的“我的剪贴”文档中，将Kindle设备连接到电脑上，在“Documents”文件夹中，有一个名为“My Clippings.txt”的文本文档，这个文档就是“我的剪贴”的所有内容。


20160420_4
将文档拷贝到电脑中，使用一款名为“KindleMate”的软件打开，即可看到所有勾画的内容文本，并以包含勾画的书名进行分类，如下图：


20160420_5
选择相应的书籍笔记，进行复制即可。

保存到笔记软件

所有的读书笔记，我都会保存到印象笔记——或者其他的笔记软件，可参考之前的两篇文章《化零为整的高效——你需要一款笔记应用》和《你早该这样记笔记——高效的印象笔记调教术》。

